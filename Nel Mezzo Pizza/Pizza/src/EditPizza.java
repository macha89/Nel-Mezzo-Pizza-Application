    int pizzaIndex=0;
    int pizzaCount=Home.newOrder.getPizzaCount();
    String size,crust,topping1,topping2,sauce;
    Pizza y=new Pizza();
        
    Pizza createReplicaPizza(Pizza x)
    {
        y.sauce=x.sauce;
        y.size=x.size;
        y.topping1=x.topping1;
        y.topping2=x.topping2;
        y.crust=x.crust;
        y.cost=y.getCost();
        return y;
    }
    
    void setPizza()
    {
        pizzaCount=Home.newOrder.getPizzaCount();
        y=createReplicaPizza(Home.newOrder.getPizza(pizzaIndex));
        jComboBox1.setSelectedIndex(y.size);
        jComboBox2.setSelectedIndex(y.crust);
        jComboBox3.setSelectedIndex(y.topping1);
        jComboBox4.setSelectedIndex(y.topping2);
        jComboBox5.setSelectedIndex(y.sauce);
        jTextArea1.setText(y.getPizzaInfo());
        jLabel7.setText("PIZZA No. "+(pizzaIndex+1));
    }
    
