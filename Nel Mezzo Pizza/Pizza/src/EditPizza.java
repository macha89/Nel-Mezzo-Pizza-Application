    private void b3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b3ActionPerformed
        Home.newOrder.deletePizza(pizzaIndex);
        pizzaCount=Home.newOrder.getPizzaCount();
        if(pizzaCount>0)
        {
            if(pizzaIndex>pizzaCount-1)    pizzaIndex=pizzaCount-1;
            setPizza();
        }
        else dispose();
    }//GEN-LAST:event_b3ActionPerformed

